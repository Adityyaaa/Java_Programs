import java.util.*;

public class pretty_printing {
    public static void main(String[] args) {
        float a = 345.345f;
        System.out.printf("Formatted number is %.2f\n", a);
        System.out.printf("Pie: %.3f\n",Math.PI);
        System.out.printf("I am %s and I am %s", "Mia","Khalifa");
    }
}
